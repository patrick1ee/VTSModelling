    Const.EEG_chan = get_chan_idx(Const, 'CP1'); 
    Const.REF_CHAN = [get_chan_idx(Const, 'Cz'), ...
                      get_chan_idx(Const, 'C3'), ...
                      get_chan_idx(Const, 'Pz'), ...
                      get_chan_idx(Const, 'P3')]; 
                  
