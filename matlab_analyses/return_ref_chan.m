    Const.EEG_chan = get_chan_idx(Const, 'C3'); 
    Const.REF_CHAN = [get_chan_idx(Const, 'FC5'), ...
                      get_chan_idx(Const, 'FC1'), ...
                      get_chan_idx(Const, 'CP5'), ...
                      get_chan_idx(Const, 'CP1')]; 

